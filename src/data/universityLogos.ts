/**
 * Returns the Google favicon service URL for the university's official website logo.
 * Google's S2 favicon CDN fetches and caches official site icons at the requested size.
 * If the domain is unknown an empty string is returned — the UI falls back to the SVG crest.
 */
export function getUniversityLogoUrl(universityName: string): string {
  const domain = universityDomains[universityName];
  if (!domain) return "";
  return `https://www.google.com/s2/favicons?domain=${domain}&sz=256`;
}

/**
 * Returns the official domain for a university (or empty string if unknown).
 */
export function getUniversityDomain(universityName: string): string {
  return universityDomains[universityName] ?? "";
}
